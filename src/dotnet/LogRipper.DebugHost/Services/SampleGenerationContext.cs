    public static SampleGenerationContext Create(string inputCallsign, QsoSampleOptions options)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(inputCallsign);
        ArgumentNullException.ThrowIfNull(options);

        var station = StationTemplates[RandomNumberGenerator.GetInt32(StationTemplates.Length)];
        var firstName = FirstNames[RandomNumberGenerator.GetInt32(FirstNames.Length)];
        var lastName = LastNames[RandomNumberGenerator.GetInt32(LastNames.Length)];
        var generationToken = Guid.NewGuid().ToString("N");
        return new SampleGenerationContext(inputCallsign, options, station, generationToken, firstName, lastName);
    }

    public static T Pick<T>(IReadOnlyList<T> items)
    {
        ArgumentNullException.ThrowIfNull(items);

        return items[RandomNumberGenerator.GetInt32(items.Count)];
    }

    public static bool NextBool()
    {
        return RandomNumberGenerator.GetInt32(0, 2) == 0;
    }

    public static int NextInt(int minInclusive, int maxExclusive)
    {
        return RandomNumberGenerator.GetInt32(minInclusive, maxExclusive);
    }

    public static double NextDouble(double minInclusive, double maxExclusive)
    {
        var fraction = RandomNumberGenerator.GetInt32(0, 1_000_000) / 1_000_000d;
        return minInclusive + (fraction * (maxExclusive - minInclusive));
    }

    public static FieldDescriptor PickOneofField(OneofDescriptor oneof)
    {
        ArgumentNullException.ThrowIfNull(oneof);

        var fields = oneof.Fields.ToArray();
        return fields[RandomNumberGenerator.GetInt32(fields.Length)];
    }
