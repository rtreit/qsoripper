    private SampleGenerationContext(
        string inputCallsign,
        QsoSampleOptions options,
        SampleStationTemplate station,
        string generationToken,
        string firstName,
        string lastName)
    {
        InputCallsign = inputCallsign;
        Options = options;
        Station = station;
        GenerationToken = generationToken;

        var profileSuffix = NextInt(11, 99);
        ProfileId = $"{station.ProfileSlug}-{profileSuffix}-{generationToken[..4]}";
        ProfileName = station.ProfileLabel;
        FirstName = firstName;
        LastName = lastName;
        OperatorName = $"{firstName} {lastName}";
        GeneratedAtUtc = DateTime.SpecifyKind(DateTime.UtcNow.AddMinutes(-NextInt(1, 180)), DateTimeKind.Utc);
        UpdatedAtUtc = GeneratedAtUtc.AddMinutes(NextInt(1, 45));
        LastModifiedUtc = GeneratedAtUtc.AddDays(-NextInt(1, 7));
        SkipCache = NextBool();
        CacheHit = !SkipCache || NextInt(0, 3) != 0;
        MakeActive = NextBool();
        HasSessionOverride = NextBool();
        SetupComplete = true;
        PreferSqlite = NextInt(0, 4) != 0;
        SessionKey = $"sample-{generationToken[..10]}";
        UserAgent = $"LogRipper/0.1.0 ({station.OperatorCallsign})";
        SqlitePath = $".\\data\\{station.ProfileSlug}-{GeneratedAtUtc:yyyyMMdd-HHmm}.db";
        LocalId = Guid.NewGuid().ToString("D");
        Comment = Pick(CommentTemplates);
        Note = Pick(NoteTemplates);
        Summary = Pick(SummaryTemplates);
        Warning = Pick(WarningTemplates);
        ErrorMessage = Pick(ErrorTemplates);
        Addr2 = station.City;
        LicenseClass = Pick(["Extra", "Advanced", "General", "Amateur Extra"]);
        TxPower = Pick(PowerLevels);
        LookupLatencyMs = NextInt(18, 250);
        HttpTimeoutSeconds = NextInt(5, 16);
        MaxRetries = NextInt(1, 4);
        QrzSerial = NextInt(100000, 9999999);
        Count = NextInt(1, 4);
        NegativeOffsetHours = -NextInt(5, 9);
        Latitude = station.Latitude + NextDouble(-0.08, 0.08);
        Longitude = station.Longitude + NextDouble(-0.08, 0.08);
    }

